    private void btnEscalacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEscalacaoActionPerformed
         if(jtTableJogos.getSelectedRow() != -1 )
       {
            Jogo j = new ConfrontoNormal();
            j = j.buscaJogo((int) jtTableJogos.getModel().getValueAt(jtTableJogos.getSelectedRow(), 5));
            frmVisualizaEscalacao ve = new frmVisualizaEscalacao(j);
            ve.setVisible(true);
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione o jogo");
        }
    }//GEN-LAST:event_btnEscalacaoActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        int c = 0;
        for(Time t : Time.getTimes())
        {
            if(t.getNome().equals(jtPesquisa.getText()))
            {
                frmDetalhesTime frmdt = new frmDetalhesTime(t);
                frmdt.setVisible(true);
                c++;
            }
        }
        if(c == 0) JOptionPane.showMessageDialog(null, "Nao encontramos o time "+ jtPesquisa.getText());
    }//GEN-LAST:event_btnPesquisarActionPerformed

