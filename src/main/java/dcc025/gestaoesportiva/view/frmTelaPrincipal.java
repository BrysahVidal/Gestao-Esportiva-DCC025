    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
      try{
           VerificaUsuario vu = new VerificaUsuario(jtLogin.getText(),jtSenha.getText());
           if(vu.getClasse().equals("Comissao"))
           {
               frmTelaComissao frmc = new frmTelaComissao(campeonato);
               frmc.setVisible(true);
               this.dispose();
           }
           else
           {
               Time t2 = new Time();
               t2 = t2.buscaTime(jtSenha.getText());
               frmTelaTecnico frmc = new frmTelaTecnico(t2,campeonato);
               frmc.setVisible(true);
               this.dispose();
           }
      }
      catch(Exception e)
      {
          JOptionPane.showMessageDialog(null, e.getMessage());
      }
    }//GEN-LAST:event_btnEntrarActionPerformed

